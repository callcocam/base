    public function setValueOption($table,$condicao=['state'=>'0'],$index="id",$value="title",$ordering=['id'=>'ASC'],$valueDefault=[''=>'']){
        $repository=$this->containerInterface->get($table)->valueOptions($condicao,$ordering);
        $options=$valueDefault;
        if($repository)
        {
            foreach($repository as $obj){
                $array=$obj->toArray();
                $options[$array[$index]]=$array[$value];
            }
        }

        return $options;
    }

