    public function setCidade()
    {
        if ($this->has('cidade')):
            if($this->get('cidade')->getAttribute('type')=="select"):
                if($this->cache->hasItem('cidade')){
                    $cidades=$this->cache->getItem('cidade');
                    $arraycidades=[''=>'-SELECIONE-'];
                    foreach($cidades as $cidade):
                        $arraycidades[$cidade['id']]=$cidade['title'];
                    endforeach;
                }
                else{
                    /**
                     * @var $client ClientHttp
                     */
                    $client = $this->containerInterface->get(Client::class);
                    $client->setUri(sprintf("%s/%s",$this->config->serverHost,'cidades'));
                    $response = $client->send();
                    if ($response->isSuccess()) {
                        $data=json_decode($response->getBody(),true);
                        $arraycidades=[];
                        $cidadeCache=[];
                        foreach($data['data'] as $o){
                            $arraycidades[$o['id']]=$o['title'];
                            $cidadeCache[$o['id']]=$o;
                        }
                        $this->cache->addItem('cidade',$cidadeCache);
                    }
                }

                $this->get('cidade')->setOptions(['value_options' => $arraycidades]);
            else:
                $this->get('cidade')->setValue('1');
            endif;
        endif;
    }

    public function week(){
        $date_start=date('2016-09-26');
        $week=[];
        for($i= 0;$i < 10;$i++){
            $new_date = strtotime(date("Y-m-d", strtotime($date_start)) . " 7 day");
            $date_stop=strtotime(date("Y-m-d", strtotime($date_start)) . " 13 day");
            $dia_start =date('d',$new_date);
            $dia_stop =date('d',$date_stop);
            $mes_start = $this->getMes(date('m',$new_date));
            $mes_stop = $this->getMes(date('m',$date_stop));
            $ano = date('Y',$new_date);
            $date_start= date("Y-m-d", $new_date);
            $date_format="SEMANA DE {$dia_start} - {$dia_stop} DE {$mes_start} DE {$ano}";
            $week[$date_format]=$date_format;
        }
        return $week;
    }


